import java.lang.Math;
public class MathLibraryPractice {
   public static void main(String args [])
   {
       int number1 = 9;
       int number2 = 2;
       double number3=10.5;
       System.out.println(Math.min(number2, number1));
       System.out.println(Math.max(number2, number1));
       System.out.println(Math.round(number3));
       System.out.println(Math.pow(number1, number2));
       System.out.println(Math.floor(number3));
       
   } 
}
